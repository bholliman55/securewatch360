-- SecureWatch360: compliance scan per-control results.
-- scan_runs remains the execution/audit boundary; this table stores one row
-- per evaluated framework control for a compliance scan.

create table if not exists public.compliance_scan_results (
  id uuid primary key default gen_random_uuid(),
  tenant_id uuid not null references public.tenants (id) on delete cascade,
  scan_run_id uuid not null references public.scan_runs (id) on delete cascade,
  framework text not null,
  control_requirement_id uuid references public.control_requirements (id) on delete set null,
  control_id text not null,
  control_name text not null,
  status text not null,
  evidence_status text not null default 'evidence_missing',
  evidence jsonb not null default '{}',
  gap text,
  recommended_action text,
  severity text not null default 'medium',
  related_asset_id uuid references public.scan_targets (id) on delete set null,
  related_scan_id uuid references public.scan_runs (id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint compliance_scan_results_status_check
    check (status in ('pass', 'fail', 'partial', 'unknown')),
  constraint compliance_scan_results_evidence_status_check
    check (evidence_status in ('available', 'evidence_missing')),
  constraint compliance_scan_results_severity_check
    check (severity in ('info', 'low', 'medium', 'high', 'critical')),
  constraint compliance_scan_results_scan_control_uniq
    unique (scan_run_id, control_id)
);

comment on table public.compliance_scan_results is
  'Per-control outcome rows generated by compliance scans. Missing evidence is unknown, never pass.';

create index if not exists compliance_scan_results_tenant_scan_idx
  on public.compliance_scan_results (tenant_id, scan_run_id);

create index if not exists compliance_scan_results_framework_status_idx
  on public.compliance_scan_results (framework, status);

create index if not exists compliance_scan_results_related_asset_idx
  on public.compliance_scan_results (related_asset_id);

alter table public.compliance_scan_results enable row level security;

drop policy if exists "compliance_scan_results_select_tenant_members"
  on public.compliance_scan_results;
create policy "compliance_scan_results_select_tenant_members"
  on public.compliance_scan_results for select to authenticated
  using (
    exists (
      select 1
      from public.tenant_users tu
      where tu.tenant_id = compliance_scan_results.tenant_id
        and tu.user_id = auth.uid()
    )
  );
