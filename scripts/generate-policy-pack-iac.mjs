/**
 * Generates IaC under iac/securewatch360-policy-pack from
 * supabase/migrations/20260425150000_policy_pack_full_catalog.sql
 * (must stay aligned with policy_framework_controls.terraform_module / ansible_role).
 *
 * Run from repo root: node scripts/generate-policy-pack-iac.mjs
 */
import fs from "fs";
import path from "path";

const ROOT = process.cwd();
const SQL = path.join(
  ROOT,
  "supabase",
  "migrations",
  "20260425150000_policy_pack_full_catalog.sql",
);
const PACK = path.join(ROOT, "iac", "securewatch360-policy-pack");
const TF_ROOT = path.join(PACK, "terraform");
const TF_MODS = path.join(TF_ROOT, "modules", "policies");
const ANS_ROOT = path.join(PACK, "ansible");
const ANS_ROLES = path.join(ANS_ROOT, "roles");
const GEN_TF = path.join(TF_ROOT, "generated");

/** @typedef {{ framework: string; control_code: string; policy_title: string; policy_body: string; enforcement_mode: string; terraform_slug: string; ansible_role_name: string }} CatalogRow */

/**
 * Parses one SQL VALUES tuple: ('a', 'b with ''quotes''', ...)
 * Top-level commas only; strings use '' for escaped quote.
 * @param {string} inner
 * @returns {string[]}
 */
function splitSqlTuple(inner) {
  const fields = [];
  let i = 0;
  const n = inner.length;
  while (i < n) {
    while (i < n && /\s/.test(inner[i])) i++;
    if (i >= n) break;
    if (inner[i] !== "'") throw new Error(`Expected quoted field at pos ${i}: ${inner.slice(i, i + 40)}`);
    i++;
    let s = "";
    while (i < n) {
      if (inner[i] === "'" && inner[i + 1] === "'") {
        s += "'";
        i += 2;
        continue;
      }
      if (inner[i] === "'") {
        i++;
        break;
      }
      s += inner[i];
      i++;
    }
    fields.push(s);
    while (i < n && /[\s,]/.test(inner[i])) i++;
  }
  return fields;
}

/**
 * @returns {CatalogRow[]}
 */
function loadCatalogFromSql() {
  const text = fs.readFileSync(SQL, "utf8");
  /** @type {CatalogRow[]} */
  const rows = [];
  for (const line of text.split(/\r?\n/)) {
    const t = line.trim();
    if (!t.startsWith("(") || !t.includes("modules/policies/")) continue;
    let body = t;
    if (body.endsWith(",")) body = body.slice(0, -1);
    if (body.endsWith(")")) body = body.slice(0, -1);
    const inner = body.slice(1);
    const f = splitSqlTuple(inner);
    if (f.length !== 7) {
      throw new Error(`Expected 7 fields, got ${f.length}: ${t.slice(0, 120)}`);
    }
    const [framework, control_code, policy_title, policy_body, enforcement_mode, tfPath, ansiblePath] = f;
    const slug = tfPath.replace(/^modules\/policies\//, "").trim();
    const roleName = ansiblePath.replace(/^roles\//, "").trim();
    if (!slug || !roleName || !roleName.startsWith("policy_")) {
      throw new Error(`Bad paths: ${tfPath} / ${ansiblePath}`);
    }
    rows.push({
      framework,
      control_code,
      policy_title,
      policy_body,
      enforcement_mode,
      terraform_slug: slug,
      ansible_role_name: roleName,
    });
  }
  const seen = new Set();
  for (const r of rows) {
    const k = r.terraform_slug;
    if (seen.has(k)) throw new Error(`Duplicate terraform slug: ${k}`);
    seen.add(k);
  }
  return rows.sort((a, b) => a.terraform_slug.localeCompare(b.terraform_slug));
}

/** Deterministic heredoc terminator (must not appear as its own line in title/body). */
function tfHeredocBlock(kind, slug, content) {
  const safeSlug = slug.replace(/[^a-z0-9_]/gi, "_");
  const delim = `SW360_${kind}_${safeSlug}`;
  return `<<-${delim}\n${content.trim().replace(/\r\n/g, "\n")}\n${delim}`;
}

function terraformModuleContents(row) {
  const s = row.terraform_slug;
  const titleH = tfHeredocBlock("TITLE", s, row.policy_title);
  const bodyH = tfHeredocBlock("BODY", s, row.policy_body);
  return `# Generated by scripts/generate-policy-pack-iac.mjs — catalog slug ${row.terraform_slug}

variable "enforcement_mode" {
  description = "advisory | enforced"
  type        = string
  default     = "advisory"
}

output "securewatch360_control" {
  description = "${row.framework} ${row.control_code}"
  value = {
    framework   = "${row.framework.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"
    code        = "${row.control_code.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"
    title       = ${titleH}
    description = ${bodyH}
    version     = 1
    mode        = var.enforcement_mode
    checklist = [
      "Map evidence collection, ownership, and automation to organizational policy",
      "Align automation hooks with SecureWatch360 policy bindings for this catalog control",
    ]
  }
}
`;
}

/** YAML-safe indented block scalar */
function ansibleTasksContent(row) {
  const fm = row.framework;
  const cc = row.control_code;
  const role = row.ansible_role_name;
  const title = row.policy_title.replace(/\r/g, "").trim();
  const bodyFirst = row.policy_body.replace(/\r/g, "").split("\n")[0]?.trim() ?? "";
  const lines = [
    "---",
    `# Generated by scripts/generate-policy-pack-iac.mjs — reference stub for catalog role ${role}`,
    `- name: "SecureWatch360 catalog — ${role} (${fm} ${cc})"`,
    "  ansible.builtin.debug:",
    "    msg: |",
    `      SecureWatch360 policy-as-code stub: ${role}`,
    `      Framework: ${fm} | Control code: ${cc}`,
    `      Title: ${title.replace(/\n/g, " ")}`,
    `      Summary: ${bodyFirst}`,
    `      Intended enforcement: {{ enforcement_mode | default('advisory') }} (matches catalog row: ${row.enforcement_mode}).`,
    "      Extend this role with evidence paths, scanners, ticketing, or guardrails.",
  ];
  return lines.join("\n") + "\n";
}

function writeMainTf(rows) {
  const slugs = rows.map((r) => r.terraform_slug);
  fs.mkdirSync(GEN_TF, { recursive: true });
  const slugPath = path.join(GEN_TF, "control_slugs.json");
  fs.writeFileSync(slugPath, JSON.stringify(slugs, null, 2) + "\n", "utf8");

  const mainPath = path.join(TF_ROOT, "main.tf");
  const header = `# SecureWatch360 Policy Pack — full catalog (${slugs.length} controls)
# Generated by scripts/generate-policy-pack-iac.mjs from 20260425150000_policy_pack_full_catalog.sql
# Reference layout for GET /api/policy/export/terraform — copy into your infra repo.

variable "enforcement_mode" {
  description = "Global enforcement mode: advisory | enforced"
  type        = string
  default     = "advisory"
}

locals {
  policy_control_slugs = jsondecode(file("\${path.module}/generated/control_slugs.json"))
}

module "policy_control" {
  for_each         = toset(local.policy_control_slugs)
  source           = "./modules/policies/\${each.key}"
  enforcement_mode = var.enforcement_mode
}

output "all_controls" {
  description = "All provisioned policy controls (SecureWatch360 catalog)"
  value       = [for k, m in module.policy_control : m.securewatch360_control]
}

output "control_count" {
  value = length(local.policy_control_slugs)
}
`;
  fs.writeFileSync(mainPath, header, "utf8");
}

function writeAnsiblePlaybook(rows) {
  const byFw = new Map();
  for (const r of rows) {
    if (!byFw.has(r.framework)) byFw.set(r.framework, []);
    byFw.get(r.framework).push(r);
  }
  const fwOrder = Array.from(byFw.keys()).sort((a, b) => a.localeCompare(b));
  const lines = [
    "---",
    `# SecureWatch360 Policy Pack — full catalog (${rows.length} Ansible roles)`,
    "# Generated by scripts/generate-policy-pack-iac.mjs — names match policy_framework_controls.ansible_role",
    "# Run: ansible-playbook playbook-securewatch360-policy-pack.yml [-e enforcement_mode=enforced]",
    "- name: SecureWatch360 policy pack (full catalog)",
    "  hosts: localhost",
    "  connection: local",
    "  gather_facts: false",
    "  vars:",
    "    enforcement_mode: advisory  # Override: enforced",
    "  roles:",
  ];
  for (const fw of fwOrder) {
    const list = byFw.get(fw).sort((a, b) => a.ansible_role_name.localeCompare(b.ansible_role_name));
    lines.push(`    # ── ${fw} (${list.length} roles) ──`);
    for (const r of list) {
      lines.push(`    - role: ${r.ansible_role_name}`);
    }
  }
  lines.push("");
  fs.writeFileSync(path.join(ANS_ROOT, "playbook-securewatch360-policy-pack.yml"), lines.join("\n"), "utf8");
}

function cleanGeneratedDirs() {
  if (fs.existsSync(TF_MODS)) {
    for (const name of fs.readdirSync(TF_MODS)) {
      fs.rmSync(path.join(TF_MODS, name), { recursive: true, force: true });
    }
  }
  if (fs.existsSync(ANS_ROLES)) {
    for (const name of fs.readdirSync(ANS_ROLES)) {
      if (name.startsWith("policy_")) {
        fs.rmSync(path.join(ANS_ROLES, name), { recursive: true, force: true });
      }
    }
  }
}

function main() {
  const rows = loadCatalogFromSql();
  console.info(`[generate-policy-pack-iac] parsed ${rows.length} catalog rows from SQL`);

  cleanGeneratedDirs();

  fs.mkdirSync(TF_MODS, { recursive: true });
  fs.mkdirSync(ANS_ROLES, { recursive: true });

  for (const r of rows) {
    const modDir = path.join(TF_MODS, r.terraform_slug);
    fs.mkdirSync(modDir, { recursive: true });
    fs.writeFileSync(path.join(modDir, "main.tf"), terraformModuleContents(r), "utf8");

    const roleDir = path.join(ANS_ROLES, r.ansible_role_name, "tasks");
    fs.mkdirSync(roleDir, { recursive: true });
    fs.writeFileSync(path.join(roleDir, "main.yml"), ansibleTasksContent(r), "utf8");
  }

  writeMainTf(rows);
  writeAnsiblePlaybook(rows);

  console.info("[generate-policy-pack-iac] wrote Terraform modules, Ansible roles, main.tf, playbook, generated/control_slugs.json");
}

main();
